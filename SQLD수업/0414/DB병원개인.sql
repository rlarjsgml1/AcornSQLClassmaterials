-- 1. 환자 테이블
CREATE TABLE HOSP_PATIENT (
    PATIENT_ID NUMBER PRIMARY KEY,
    PATIENT_NAME VARCHAR2(50) NOT NULL,
    BIRTH_DATE DATE,
    GENDER CHAR(1),
    PHONE VARCHAR2(20),
    ADDRESS VARCHAR2(200)
);

-- 2. 진료과 테이블
CREATE TABLE HOSP_DEPARTMENT (
    DEPARTMENT_ID NUMBER PRIMARY KEY,
    DEPARTMENT_NAME VARCHAR2(50) NOT NULL
);

-- 3. 의사 테이블
CREATE TABLE HOSP_DOCTOR (
    DOCTOR_ID NUMBER PRIMARY KEY,
    DOCTOR_NAME VARCHAR2(50) NOT NULL,
    PHONE VARCHAR2(20),
    DEPARTMENT_ID NUMBER,
    CONSTRAINT FK_HDOCTOR_DEPARTMENT
        FOREIGN KEY (DEPARTMENT_ID)
        REFERENCES HOSP_DEPARTMENT(DEPARTMENT_ID)
);

-- 4. 의사커리어 테이블
CREATE TABLE HOSP_DOCTOR_CAREER (
    CAREER_ID NUMBER PRIMARY KEY,
    DOCTOR_ID NUMBER NOT NULL,
    CAREER_CONTENT VARCHAR2(255),
    CONSTRAINT FK_HCAREER_DOCTOR
        FOREIGN KEY (DOCTOR_ID)
        REFERENCES HOSP_DOCTOR(DOCTOR_ID)
);

-- 5. 예약 테이블
CREATE TABLE HOSP_RESERVATION (
    RESERVATION_ID NUMBER PRIMARY KEY,
    PATIENT_ID NUMBER NOT NULL,
    DOCTOR_ID NUMBER NOT NULL,
    RESERVATION_DATE DATE,
    RESERVATION_STATUS VARCHAR2(20),
    SYMPTOM_CONTENT VARCHAR2(255),
    CONSTRAINT FK_HRESERVATION_PATIENT
        FOREIGN KEY (PATIENT_ID)
        REFERENCES HOSP_PATIENT(PATIENT_ID),
    CONSTRAINT FK_HRESERVATION_DOCTOR
        FOREIGN KEY (DOCTOR_ID)
        REFERENCES HOSP_DOCTOR(DOCTOR_ID)
);

-- 6. 진료내역 테이블
CREATE TABLE HOSP_MEDICAL_RECORD (
    RECORD_ID NUMBER PRIMARY KEY,
    RESERVATION_ID NUMBER UNIQUE,
    DIAGNOSIS_NAME VARCHAR2(100),
    TREATMENT_CONTENT VARCHAR2(1000),
    PRESCRIPTION_CONTENT VARCHAR2(1000),
    TREATMENT_DATE DATE,
    CONSTRAINT FK_HRECORD_RESERVATION
        FOREIGN KEY (RESERVATION_ID)
        REFERENCES HOSP_RESERVATION(RESERVATION_ID)
);


